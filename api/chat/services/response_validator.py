from ..core.config import log
from .gigachat_service import handle_gigachat_request


async def validate_response_with_agent(draft_response: str, registry_data: str, **kwargs):
    """
    Агент-контролер: проверяет черновик ответа на соответствие фактам из реестра.
    """
    try:
        validation_messages = [
            {"role": "system", "content": (
                "ТЫ — КОНТРОЛЕР КАЧЕСТВА И ФАКТЧЕКЕР. Твоя задача: очистить ответ бота от галлюцинаций и выдуманных данных.\n\n"
                "ЭТАЛОННЫЕ ДАННЫЕ (РЕЕСТР):\n"
                f"{registry_data}\n\n"
                "ПРАВИЛА ПРОВЕРКИ:\n"
                "1. Сравни черновик ответа с РЕЕСТРОМ.\n"
                "2. Если в черновике есть ТЕЛЕФОН, EMAIL, ССЫЛКА или конкретный ФАКТ О БИЗНЕСЕ, которого НЕТ в реестре — УДАЛИ его или замени на фразу 'информация не указана'.\n"
                "3. КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО оставлять выдуманные почты (например, info@домен) или телефоны.\n"
                "4. Если черновик содержит ложь о графике работы или реквизитах — исправь согласно реестру.\n"
                "5. Сохраняй стиль, вежливость и обычную человеческую беседу (приветствия, вопросы о делах). Правила проверки фактов НЕ касаются вежливых фраз и светской беседы.\n"

                "6. КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО добавлять фразы о том, что информация предоставлена в 'учебных целях' или 'не для финансовых операций'. Это реальный бизнес-ассистент.\n"
                "7. Верни только исправленный текст ответа, без своих комментариев.\n"
                "8. ОБЯЗАТЕЛЬНО сохраняй язык оригинала. Если черновик на русском — отвечай на русском, если на английском — на английском."
            )},
            {"role": "user", "content": f"ЧЕРНОВИК ОТВЕТА ДЛЯ ПРОВЕРКИ:\n{draft_response}"}
        ]

        validated_response = await handle_gigachat_request(validation_messages, "GigaChat", stream=False, **kwargs)
        return validated_response if validated_response else draft_response
    except Exception as e:
        log.error(f"Validation Agent Error: {e}")
        return draft_response
